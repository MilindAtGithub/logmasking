    @Override
    public void debug(String s) {

        LoggerFactory.getLogger(this.loggingClass).debug(
                MaskingUtil.isBracesPresent(s)?"Log will not be written":s);
    }

    @Override
    public void debug(String s, Object o) {

        long start = Calendar.getInstance().getTimeInMillis();
        LoggerFactory.getLogger(this.loggingClass).debug(
                MaskingUtil.isBracesPresent(s)?"Log will not be written":s+" {}",MaskingUtil.mask(o));
        System.out.println("Time For Logging in Millisec : "+ (Calendar.getInstance().getTimeInMillis() -start));
    }

    @Override
    public void debug(String s, Object o, Object o1) {

        LoggerFactory.getLogger(this.loggingClass).debug(
                MaskingUtil.isBracesPresent(s)?"Log will not be written":s+" {} "+" {} ", MaskingUtil.mask(o),
                MaskingUtil.mask(o1));
    }

    @Override
    public void debug(String s, Object... objects) {

        LoggerFactory.getLogger(this.loggingClass).debug(s,objects);
    }

    @Override
    public void debug(String s, Throwable throwable) {

        LoggerFactory.getLogger(this.loggingClass).debug(
                MaskingUtil.isBracesPresent(s)?throwable.getMessage():s,throwable);
    }

